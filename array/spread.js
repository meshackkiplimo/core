//spread operator
//[...array]
//returns a new array with all elements from the original array
 let numbers = [1, 2, 3, 4, 5];
 let newNumbers = [...numbers];
//  console.log(newNumbers); // [1, 2, 3, 4, 5

 // combining arrays
 let numbers1 = [1, 2, 3];
 let numbers2 = [4, 5, 6];
 let combined = [...numbers1, ...numbers2];
 console.log(combined); // [1, 2, 3, 4, 5
