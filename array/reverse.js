// reverse()
// reverse() reverses the order of the elements in the list.
// It does not modify the original list, but returns a new list with the elements in reverse order
// The original list remains unchanged
// The new list is a copy of the original list, but with the elements in reverse order
// The new list is a list of the same type as the original list
// The new list has the same length as the original list
// The new list has the same elements as the original list, but in reverse order
const num =  [1, 2, 3, 4, 5];
const reversedNum = num.reverse();
console.log(num);
console.log(reversedNum);
